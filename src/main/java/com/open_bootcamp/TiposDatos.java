package com.open_bootcamp;

public class TiposDatos {

    public static void main( String[] args ) {

        String nombre = "Fredy";
        int edad = 32;
        long numeroCelular = 311875078;
        double salario = 4000000;
        boolean matrimonio = false;

        System.out.println("El tipo de dato STRING tiene el valor -> " + nombre);
        System.out.println("El tipo de dato INT tiene el valor -> " + edad);
        System.out.println("El tipo de dato LONG tiene el valor -> " + numeroCelular);
        System.out.println("El tipo de dato DOUBLE tiene el valor -> " + salario);
        System.out.println("El tipo de dato BOOLEAN tiene el valor -> " + matrimonio);
    }
}
